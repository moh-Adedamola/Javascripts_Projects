

let left_operand = 10;
let right_operand = 5;
let operator = "/";
let answer;	


if (operator == "+"){
	answer = (left_operand + right_operand);
}else if (operator == "-"){
	answer = (left_operand - right_operand);
	}
else if (operator == "*"){
	answer = (left_operand * right_operand);
	}
else if (operator == "/"){
	answer = (left_operand / right_operand);
}

console.log(answer);



